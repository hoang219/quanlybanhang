package model;

/**
 *
 * @author hoang
 */
public class MatHang {
    private int id;
    private String ten;
    private double dongia;

    public MatHang() {
        super();
    }

    public MatHang(int id, String ten, double dongia) {
        this.id = id;
        this.ten = ten;
        this.dongia = dongia;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public double getDongia() {
        return dongia;
    }

    public void setDongia(double dongia) {
        this.dongia = dongia;
    }
}
